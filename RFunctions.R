add <- function(x, y) {
x + y
}
add(4,5)

subtract <- function(x, y) {
x - y
}
subtract(4,5)

multiply <- function(x, y) {
  x * y
}
multiply(4,5)

divide <- function(x, y) {
  x / y
}
divide(4,5)

squared <- function(x) {
  x ** 2
}
squared(18)

exponential <- function(x, y) {
  x ** y
}
exponential(4,5)

root <- function(x, y) {
  x ** (1/y)
}
root(4,5)

sqrt <- function(x) {
  x ** 0.5
}
sqrt(49)

modulus10 <- function(x) {
  x %% 10  
}  
modulus10(34)

squared_difference <- function(x,y){
  (x-y)**2
}
squared_difference(4,10)
